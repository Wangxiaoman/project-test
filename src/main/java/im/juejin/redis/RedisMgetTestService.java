    public Map<String, Object> getResult(int number) {
        Map<String, Object> result = new HashMap<>();
        List<String> keys = getKeys();
        Collections.shuffle(keys);
        long allCost = 0;
        for(int i=0;i<number;i++){
            int index = RandomUtils.nextInt(0, keys.size());
            long beginTime = System.nanoTime();
            String key = keys.get(index);
            stringRedisTemplate.opsForValue().get(key);
            long endTime = System.nanoTime();
            long cost = ((endTime - beginTime)/1000);
            if(i % 100 == 0){
                result.put("key" + i + ":" + key, "cost:" + cost);
            }
            allCost += cost;
        }
        result.put("number", number);
        result.put("cost", allCost);
        result.put("cost average", allCost/number);

        return result;
    }

